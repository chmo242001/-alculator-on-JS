function addition (x, y) {
    return x + y
    
}

function substraction (x, y) {
    return x - y

}

function multiplication (x, y) {
    return x * y

}

function division (x, y) {
    return x / y

}

function sinus (y) {
    return Math.sin (y)

}

function cosine (y) {
    return Math.cos (y)

}

function tangent (y) {
    return Math.tan (y)

}

function radix (y) {
    return Math.sqrt (y)

}
